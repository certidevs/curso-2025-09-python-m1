
"""
Operadores de membresía para evaluar si un elemento está contenido 
en otro elemento:

in
not in

Se usa habitualmente para string y para listas o estructuras de datos

"""

nombre = 'Alan'
nombre_completo = 'Alan Sastre'
print(nombre in nombre_completo) # True


nombre = 'Pepe'
nombre_completo = 'Alan Sastre'
print(nombre in nombre_completo) # False