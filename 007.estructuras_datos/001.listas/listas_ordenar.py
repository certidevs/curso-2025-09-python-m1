numeros = [3, 1, 4, 2]
print(numeros)
numeros.sort()
print(numeros)  # [1, 2, 3, 4]

# Orden descendente
numeros.sort(reverse=True)
print(numeros)  # [4, 3, 2, 1]

print()

# Ordenar cadenas
palabras = ["zebra", "avión", "casa"]
print(palabras)
palabras.sort()
print(palabras)  # ["avión", "casa", "zebra"]

print()

letras = ["a", "b", "c", "e"]
print(letras)
letras.reverse()
print(letras)  # ["e", "c", "b", "a"]

print()